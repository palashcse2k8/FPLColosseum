package com.infotech.fplcolosseum.features.homepage.models.picks;

public class AutomaticSubs {
    private long entry;
    private long element_in;
    private long element_out;
    private long event;


    // Getter Methods
    public long getEntry() {
        return entry;
    }

    public long getElement_in() {
        return element_in;
    }

    public long getElement_out() {
        return element_out;
    }

    public long getEvent() {
        return event;
    }

    // Setter Methods
    public void setEntry(long entry) {
        this.entry = entry;
    }

    public void setElement_in(long element_in) {
        this.element_in = element_in;
    }

    public void setElement_out(long element_out) {
        this.element_out = element_out;
    }

    public void setEvent(long event) {
        this.event = event;
    }
}
